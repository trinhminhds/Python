# capitalize = Ham Viet hoa chu cai dau
a = 'Trinh Ngoc Minh'
b = a.capitalize()
print(b)  
# upper = Ham viet hoa tat ca cac chu cai
c = a.upper()
print(c)
# lower = Ham viet thuong tat ca cac chu cai
x = a.lower()
print (x)
# swapcase = Ham biet doi chu hoa thanh chu thuong
z = a.swapcase()
print (z)
# title = Viet Hoa chu cai dau
q = a.title()
print(q)
# center = Can giua
w = a.center(40,'-')
print (w)
# rjust = can phai
s = a.rjust(40,'-') 
print(s)
# ljust = can trai
f = a.ljust(40,'-')
print  (f)
# encode = co the viet tieng viet

# replace(chu bi thay the, chu thay the ) = thay the  
r = a.replace('i','a')
print (r)

# strip = bo di cac khoang trang, cat chu
y = a.strip('T')
print(y)